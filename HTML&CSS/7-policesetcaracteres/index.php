Déclaration d'une police avec @font-face<br>
La règle CSS @font-face{} permet d'importer un/des fichiers de polices de caractères, notamment quand on a des polices spéciales qui ne sont pas présentes sur le système d'exploitation de l'internaute.<br>

Google font<br>
Google Fonts est un service d’hébergement gratuit de polices d’écritures pour le Web, démarré en 2010. Ces polices sont sous licences libres dont principalement la licence Apache et la SIL OFL.<br>

Les fonts-icons, c’est quoi ?<br>
Ce sont des polices de caractères qui permettent d’afficher des icônes, des pictogrammes sur un site Web. Crée initialement pour être utilisée avec Bootstrap, elle peut être utilisée sur n’importe quel projet. De nombreuses icônes sont gratuites, personnalisables et redimensionnables à souhait.<br>

